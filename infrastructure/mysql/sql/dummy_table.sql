CREATE TABLE user (
    id CHAR(26) PRIMARY KEY,
    authentication_id CHAR(26),
    last_name VARCHAR(60) NOT NULL,
    first_name VARCHAR(60) NOT NULL,
    created_user CHAR(26) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_user CHAR(26) NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (authentication_id) REFERENCES authentication(id) ON DELETE CASCADE
);
